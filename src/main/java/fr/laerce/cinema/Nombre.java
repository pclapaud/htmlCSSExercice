package fr.laerce.cinema;

public class Nombre {
    String affichage;
    Integer nombre;

    public Nombre(String affichage, Integer nombre) {
        this.affichage = affichage;
        this.nombre = nombre;
    }

    public String getaffichage() {
        return affichage;
    }

    public Integer getnombre() {
        return nombre;
    }
}
